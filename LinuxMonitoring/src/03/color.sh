#!/bin/bash


reset='\033[0m' 

case "$1" in
1) b='\033[107m' ;;
2) b='\033[101m' ;;
3) b='\033[102m' ;;
4) b='\033[106m' ;;
5) b='\033[105m' ;;
6) b='\033[40m' ;;
# *) echo "\"$1\" is not an option" ;;
esac

case "$2" in
1) a='\033[97m' ;;
2) a='\033[91m' ;;
3) a='\033[92m' ;;
4) a='\033[96m' ;;
5) a='\033[95m' ;;
6) a='\033[30m' ;;
# *) echo "\"$2\" is not an option" ;;
esac

case "$3" in
1) d='\033[107m' ;;
2) d='\033[101m' ;;
3) d='\033[102m' ;;
4) d='\033[106m' ;;
5) d='\033[105m' ;;
6) d='\033[40m' ;;
# *) echo "\"$3\" is not an option" ;;
esac

case "$4" in
1) c='\033[97m' ;;
2) c='\033[91m' ;;
3) c='\033[92m' ;;
4) c='\033[96m' ;;
5) c='\033[95m' ;;
6) c='\033[30m' ;;
# *) echo "\"$4\" is not an option" ;;
esac